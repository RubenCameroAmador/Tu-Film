    public boolean verificarUsuario(String archivo, String nombre){
        try{
        FileReader fr= new FileReader(archivo);
        BufferedReader br= new BufferedReader(fr);
        String linea = br.readLine();
        String[] campos;
        while(linea!=null){
            campos= linea.split(",");
            if(nombre.equals(campos[0])){
                return true;
            }else{
                linea = br.readLine();
            }
        }
        br.close();
        fr.close();
        
        }catch (Exception e){
        }
        return false;
    }
