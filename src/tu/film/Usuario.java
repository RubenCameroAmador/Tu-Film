    public void leerRegistro(String archivo, String usuario, boolean confirmar){
        try {
            FileReader fr= new FileReader(archivo);
            BufferedReader br= new BufferedReader(fr);
            String line = br.readLine();
            String[] campos;
            while(line!=null){
                campos= line.split(",");
                if(usuario.equals(campos[0])){
                    confirmar= true;
                }else{
                    line = br.readLine();
                }
            }
            br.close();
            fr.close();
        } catch (Exception e) {
        }
    }
